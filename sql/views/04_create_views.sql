    pc.SECTOR,
    da.METRIC_DATE,
    da.CHANNEL_NAME,
    da.WEBSITE_TRAFFIC,
    da.UNIQUE_VISITORS,
    da.BOUNCE_RATE,
    da.CONVERSION_RATE,
    da.AVG_ORDER_VALUE,
    da.CART_ABANDON_RATE,
    da.EMAIL_OPEN_RATE,
    da.EMAIL_CLICK_RATE,
    da.PAID_ROAS,
    da.ORGANIC_TRAFFIC_PCT,
    da.MOBILE_TRAFFIC_PCT,

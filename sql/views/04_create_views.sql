CREATE OR REPLACE VIEW V_BRAND_STRATEGY_KNOWLEDGE AS
SELECT
    bsd.DOC_ID,
    pc.COMPANY_NAME,
    pc.SECTOR,
    bsd.TITLE,
    bsd.CATEGORY,
    bsd.CONTENT,
    bsd.AUTHOR,
    bsd.PUBLISH_DATE,
    bsd.TAGS
FROM BRAND_STRATEGY_DOCS bsd
LEFT JOIN PORTFOLIO_COMPANIES pc ON bsd.COMPANY_ID = pc.COMPANY_ID;

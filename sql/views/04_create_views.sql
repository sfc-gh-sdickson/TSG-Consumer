CREATE OR REPLACE VIEW V_OPERATIONAL_EFFICIENCY AS
SELECT
    pc.COMPANY_ID,
    pc.COMPANY_NAME,
    pc.SECTOR,
    pc.SUB_SECTOR,
    om.METRIC_DATE,
    om.INVENTORY_TURNOVER,
    om.DAYS_SALES_OUTSTANDING,
    om.SUPPLY_CHAIN_SCORE,
    om.FULFILLMENT_RATE,
    om.ON_TIME_DELIVERY,
